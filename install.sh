#!/bin/bash

# Проверяем, существует ли файл g в текущей папке
if [ ! -f "./g" ]; then
    echo "❌ Файл 'g' не найден! Убедитесь, что находитесь в правильной папке."
    exit 1
fi

echo "⚙️  Подготовка утилиты..."
chmod +x ./g

echo "🔑 Установка в /usr/local/bin (может потребоваться пароль администратора)..."
sudo cp ./g /usr/local/bin/g
sudo chmod +x /usr/local/bin/g

echo "✅ Установка завершена! Введите 'g help' для проверки."